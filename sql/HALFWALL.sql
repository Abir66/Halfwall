	END IF;
END;
/
CREATE TRIGGER "HALFWALL"."STUDENT_ID_TRG" BEFORE INSERT OR UPDATE OF "STUDENT_ID" ON "HALFWALL"."USERS" REFERENCING OLD AS "OLD" NEW AS "NEW" FOR EACH ROW 
DECLARE
	DEPT_STR VARCHAR2(255);
BEGIN
  
	DEPT_STR := SUBSTR(:NEW.STUDENT_ID,3,2);
	
	
	IF DEPT_STR = '01' THEN :NEW.DEPARTMENT := 'ARCHI';
	ELSIF DEPT_STR = '02' THEN :NEW.DEPARTMENT := 'ChE';
	ELSIF DEPT_STR = '04' THEN :NEW.DEPARTMENT := 'CE';
	ELSIF DEPT_STR = '05' THEN :NEW.DEPARTMENT := 'CSE';
	ELSIF DEPT_STR = '06' THEN :NEW.DEPARTMENT := 'EEE';
	ELSIF DEPT_STR = '08' THEN :NEW.DEPARTMENT := 'IPE';
	ELSIF DEPT_STR = '10' THEN :NEW.DEPARTMENT := 'ME';
	ELSIF DEPT_STR = '11' THEN :NEW.DEPARTMENT := 'MME';
	ELSIF DEPT_STR = '12' THEN :NEW.DEPARTMENT := 'NAME';
	ELSIF DEPT_STR = '15' THEN :NEW.DEPARTMENT := 'URP';
	ELSIF DEPT_STR = '16' THEN :NEW.DEPARTMENT := 'WRE';
	ELSIF DEPT_STR = '18' THEN :NEW.DEPARTMENT := 'BME';
	ELSE  :NEW.DEPARTMENT := NULL;
	END IF;
	
END;
/

